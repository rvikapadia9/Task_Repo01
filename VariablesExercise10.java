//Write a program that uses multiple variables (`int`, `double`, `String`) to print a student's details (Name, Age, Marks).
package Variable_Exercise;

public class VariablesExercise10 {
    public static void main(String[] args) {
        String name = "Ravi";
        int age = 22;
        double marks = 88.5;

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
    }
}
