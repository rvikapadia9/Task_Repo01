//Write a program to declare all primitive datatypes in Java (byte, short, int, long, float, double, char, boolean) and print their values.
package Exercise;

public class DataTypesExercise1 {
    public static void main(String[] args) {
        byte b = 10;
        short s = 200;
        int i = 5000;
        long l = 123456789L;
        float f = 12.34F;
        double d = 123.456;
        char c = 'A';
        boolean bool = true;

        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("int: " + i);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + c);
        System.out.println("boolean: " + bool);
    }
}
