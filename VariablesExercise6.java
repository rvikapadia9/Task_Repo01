//Declare two variables for Celsius and Fahrenheit, convert Celsius to Fahrenheit using formula, and print result.
package Variable_Exercise;

public class VariablesExercise6 {
    public static void main(String[] args) {
        double celsius = 25;
        double fahrenheit = (celsius * 9 / 5) + 32;
        System.out.println("Celsius: " + celsius + " = Fahrenheit: " + fahrenheit);
    }
}
