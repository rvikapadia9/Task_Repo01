//Demonstrate **type casting** by converting a `float` to an `int`.
package Exercise;

public class DataTypesExercise9 {
    public static void main(String[] args) {
        float f = 12.75f;
        int i = (int) f;
        System.out.println("Float: " + f);
        System.out.println("After casting to int: " + i);
    }
}

