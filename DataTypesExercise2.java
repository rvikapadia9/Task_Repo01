//Convert a double value (e.g., 12.34) into an int and print both before and after conversion
package Exercise;

public class DataTypesExercise2 {
    public static void main(String[] args) {
        double d = 12.34;
        int i = (int) d;
        System.out.println("Double: " + d);
        System.out.println("Converted Int: " + i);
    }
}
